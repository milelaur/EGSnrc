                  I0 = I
                  J0 = J
                  K0 = K
                END IF
              END IF
              IF ((LA_TYPE .EQ. 2)) THEN
                IF ((ABS(DIFF*2/(DOSE(NDIST1,I,J,K)+DOSE(NDIST2,I,J,K)))
     *           .GT.DIFFMAX)) THEN
                  DIFFMAX = ABS(DIFF*2/ (DOSE(NDIST1,I,J,K)+DOSE(NDIST2,
     *            I,J,K)))
                  I0 = I
                  J0 = J
                  K0 = K
                END IF
              END IF
              IF ((LA_TYPE .EQ. 4)) THEN
