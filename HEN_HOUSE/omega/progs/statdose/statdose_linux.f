              DIFFSQ = DIFF**2
              ERRDIFFSQ = ERRDOSE(NDIST1,I,J,K)*ERRDOSE(NDIST1,I,J,K)+ E
     *        RRDOSE(NDIST2,I,J,K)*ERRDOSE(NDIST2,I,J,K)
              IF ((LA_TYPE .EQ. 2)) THEN
                DIFF = DIFF*2*100./(DOSE(NDIST1,I,J,K)+DOSE(NDIST2,I,J,K
