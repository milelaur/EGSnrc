        JLO = JMAX(NDIST2)/2
        JHI = JMAX(NDIST2)/2+1
      ELSE
        JLO = JMAX(NDIST2)/2+1
        JHI = JMAX(NDIST2)/2+1
      END IF
      DO 2141 K=1,KMAX(NDIST2)
        DOSE_STORE = 0
